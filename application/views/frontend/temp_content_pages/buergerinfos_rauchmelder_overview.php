                <h1 class="first">RAUCHWARNMELDER werden in Hessen zur PFLICHT</h1>
                <p>
                    Durch eine verbindliche Regelung in der Hessischen Bauordnung wird die Verpflichtung für den Einbau von Rauchwarnmeldern in die Verantwortung derjenigen gelegt, 
                    die am Bau und Betrieb von Wohnungen beteiligt sind. Der Einbau von Rauchwarnmeldern auf freiwilliger Basis wird dadurch ergänzt und nachhaltig beschleunigt, 
                    um so die Zahl der Toten bei Wohnungsbränden in einem absehbaren Zeitraum deutlich weiter zu reduzieren. 
                    In Wohnungen müssen Schlafräume und Kinderzimmer sowie Flure, über die Rettungswege von Aufenthaltsräumen führen, jeweils mindestens einen Rauchwarnmelder haben. 
                    Die Rauchwarnmelder müssen so eingebaut oder angebracht und betrieben werden, dass Brandrauch frühzeitig erkannt und gemeldet wird. 
                </p>
                <p>
                    Bestehende Wohnungen sind <strong>bis zum 31. Dezember 2014</strong> entsprechend auszustatten. Damit wurde in Hessen der weitreichendste Schutz der deutschen 
                    Bundesländer festgeschrieben. Dieses Gesetz basiert auf Initiativen der Feuerwehrverbände, insbesondere des Landesfeuerwehrverbandes Hessen und aller Parteien des 
                    Hessischen Landtages.
                </p>
               	<h1>Warum Rauchwarnmelder so sinnvoll sind:</h1>
                <p>
                	In der Bundesrepublik Deutschland sterben pro Jahr mehr als vierhundert Menschen durch Feuer und vor allem durch Rauch. Die meisten Brandopfer (ca. 70 Prozent) 
                    verunglücken nachts in den eigenen vier Wänden. Über 5.000 Menschen werden bei Bränden verletzt, die Sachschäden betragen Millionen. Besonders gefährlich ist der 
                    bei Wohnungsbränden entstehende Rauch, weil er sich um ein Vielfaches schneller verbreitet als Feuer. Schon in wenigen Sekunden kann die gewohnte Umgebung so 
                    stark verrauchen, dass Lebensgefahr besteht. 95 Prozent der Brandtoten sterben an den Folgen einer Rauchvergiftung! Ursache für die etwa 200.000 Brände im Jahr 
                    ist aber im Gegensatz zur landläufigen Meinung nicht nur Fahrlässigkeit. Sehr oft lösen technische Defekte Brände aus.
                </p>
                <p><img src="<?=base_url('images/content/content_rauchmelder.jpg')?>" />
                <p>
                    Trotz gutem baulichen Brandschutz und aller Vorsicht und Aufmerksamkeit, ist letztlich niemand davor geschützt, dass nicht auch in der eigenen Wohnung ein Brand 
                    entstehen kann. Ein Defekt an einer elektrischen Leitung oder einem Gerät, eine Unachtsamkeit im Haushalt, eine brennende Zigarette im Bett oder mit Feuer spielende 
                    Kinder so entsteht schnell ein Brand, der häufig nicht sofort bemerkt wird. Oft schwelen solche Brandherde über Stunden. Sind Möbel oder andere Gegenstände mit 
                    hohen Kunststoffanteilen betroffen, entwickelt sich beim Verbrennen besonders giftiger Rauch. Wer im Schlaf überrascht wird, kann durch die Rauchgase Kohlenmonoxid, 
                    Chlor oder Blausäuregas das Bewusstsein verlieren. Im schlimmsten Fall erstickt man, ohne auch nur einen Hustenreiz zu verspüren. 
                </p>
                <p>
                    Rauchmelder können in solchen Situationen Leben retten. Durch einen lauten Warnton werden Menschen selbst aus tiefstem Schlaf geweckt. So verschaffen Rauchwarnmelder 
                    die nötige Zeit, um sich und Ihre Familie in Sicherheit bringen zu können und die Feuerwehr zu rufen oder möglicherweise einen Entstehungsbrand selbst zu bekämpfen.
                </p>
               	<h1>Tipps für den Einkauf:</h1>
                <ul>
                	<li>Sie erhalten Rauchwarnmelder in Elektrofachgeschäften, Baumärkten und den Technikabteilungen von Kaufhäusern.</li>
                	<li>Das Gerät sollte auf optischer Basis funktionieren.</li>
                	<li>Batteriebetriebene Geräte sollten schwache Batterien anzeigen (durchschnittliche Lebensdauer der Batterie ca. 1 -3 Jahre).</li>
                	<li>Das Gerät sollte ein CE-Zeichen aufweisen und geprüft sein (GS-Zeichen).</li>
                    <li>Die Geräte sollten über einen Testknopf verfügen.</li>
                    <li>Markenhersteller gewähren mehrjährige Garantien</li>
          		</ul>
                <h1>Wo werden Rauchmelder installiert?</h1>
                <p><img src="<?=base_url('images/content/content_rauchmelder_haus.jpg')?>" />
				<p>
                    Die meisten Wohnungsbrände beginnen im Wohnbereich.
                    Deswegen fordert die europaweit gültige Norm zur Installation von Rauchwarnmeldern die Installation je eines Rauchwarnmelders an den Zimmerdecken aller Schlafräume, Kinderzimmer und Rettungswege (Flure). Bei mehrstöckigen Häusern sollte ein Rauchwarnmelder in jeder Etage erfolgen. (Mindestschutz). Wichtig ist es, die Geräte an der richtigen Stelle anzubringen. Die Montage erfolgt möglichst zentral im Raum, wobei zu Balken oder anderen vorspringenden Bauteilen ein Mindestabstand von 50 cm eingehalten werden sollte. Zu Leuchtstoffröhren sollte zur Vermeidung von Fehlfunktionen ebenfalls ein Abstand von 50 cm eingehalten werden. Rauchwarnmelder sollten im Flur in Küchentürnähe und/oder in der Küche mit Stummschaltung installiert werden.
                    Für mehrstöckige Häuser oder Wohnungen empfehlen sich besonders vernetzte Rauchwarnmelder, per Funk oder bei einem Neubau oder einer Sanierung auch als 230 V-Melder. Dies bedeutet, dass alle miteinander verbundenen Melder Alarm geben, wenn einer der Melder auslöst.
                    Optimalen Schutz erzielen Sie, indem Sie jedes Zimmer und den Flur in jedem Stockwerk mit je einem Rauchwarnmelder ausrüsten. Dabei sollten Keller und Dachboden nicht vergessen werden.
           		</p>
