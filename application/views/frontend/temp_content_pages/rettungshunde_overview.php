 	                     <div class="article" id="anker_ausbildung">
							<h1>Ablauf der Ausbildung</h1>
                            <p>
                                Die Ausbildung der Rettungshunde bei der Feuerwehr Bad Soden erfolgt gemäß den «Mindeststandards Rettungshunde-Ortungstechnik des Deutschen Feuerwehrverbandes» 
                                (siehe <a href="http://www.dfv.org/ak-rettungshunde.html" target="_blank">http://www.dfv.org/ak-rettungshunde.html</a>). Der Hund sollte bei Ausbildungsbeginn nicht älter als 4 Jahre sein. Ohne Vorkenntnisse ist mit einer Ausbildungsdauer 
                                von grob 2 Jahren bis zum Erreichen der lokalen Einsatzfähigkeit (RH2) zu rechnen.
                            </p>
                            <p>    
                                Die Ausbildung erfolgt zu Beginn ausschließlich im Verein für Deutsche Schäferhunde in Bad Soden (<a href="http://www.svog-badsoden.de" target="_blank">http://www.svog-badsoden.de</a>). 
                                Der angehende Rettungshund lernt spielerisch, Personen zu suchen, ungewohntes Terrain zu begehen, Hindernisse zu überwinden und bereitet sich zusammen mit seinem 
                                Hundeführer auf das Ablegen einer Begleithundeprüfung vor. In dieser zeigt der Hund den Grundgehorsam gegenüber seinem Hundeführer, sowie die Unvoreingenommenheit 
                                gegenüber anderen Menschen und Hunden.
                            </p>
                            <p>
                                Der Hundeführer tritt, falls er mit seinem Hund eine Einsatztätigkeit anstrebt, einer Feuerwehr bei und absolviert dort seine Feuerwehrgrundausbildung. 
                                Des Weiteren erhält er unter anderem eine Ausbildung in den Bereichen Suchtaktik, Funken, Erste Hilfe Hund und Mensch, Kynologie und Navigation.
                            </p>
                            <p><img src="<?=base_url('images/content/content_rettungshunde_ausbildung.png')?>" /></p>
                            <p>    
                                Sobald der Hund über Grundfertigkeiten in den Bereichen Gehorsam, Gewandtheit und Sucharbeit verfügt, demonstriert er diese in einer Rettungshundeprüfung RH1. 
                                Aus dieser folgt noch keine Einsatztauglichkeit, sie dient lediglich als Zwischenprüfung.
                            </p>
                            <p>
                                Mit Absolvieren einer Rettungshundeprüfung RH2 Trümmer oder Fläche, bei der Hund und Hundeführer die erfolgreiche Sucharbeit mit einem Truppmann unter Einsatzbedingungen zeigen, 
                                ist das Team aus Hund und Hundeführer für lokale und regionale Einsätze in der jeweiligen Sparte bereit und kommt bei Einsätzen im Main-Taunus-Kreis und angrenzenden Kreisen zum Einsatz. 
                                Die RH2 Prüfung muss jährlich wiederholt werden.
                            </p>
                            <p>    
                                Besonders geeignete Teams aus Hund und Hundeführer können sich darüber hinaus mit Absolvieren einer Rettungshundeprüfung RH3 Trümmer oder Fläche für internationale Einsätze qualifizieren. 
                                Mit einem Prüfungszeitraum von 24 Stunden (Fläche) bzw. 36 Stunden (Trümmer) fordern diese Prüfungen von Hund und Hundeführer weit über den Durchschnitt hinausgehende Leistungen. 
                                Die RH3 Prüfungen genügen den international anerkannten Standards der INSARAG (<a href="http://www.insarag.org" target="_blank">http://www.insarag.org</a>).                    
                            </p>
