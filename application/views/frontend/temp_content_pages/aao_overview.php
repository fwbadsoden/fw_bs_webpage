        <h1>Unser Alarm- und Ausrückeordnung</h1>
        <p>
        Die Alarmplan- und AusrückeOrdnung regelt, wie die Feuerwehr alarmiert, und mit welchen Fahrzeugen ausgerückt wird; geordnet nach Einsatzstichworten. Einige Beispiele:
        </p>
        <p id="feuer">
            Meldung <strong>'Feuer in Gebäude, Menschenleben in Gefahr'</strong> im Stadtgebiet Bad Soden am Taunus. Daraufhin wird von der Leitstelle Vollalarm für die Feuerwehr Bad Soden am Taunus ausgelöst, außerdem werden zwei Rettungswagen, ein Notarzt, der DRK-Ortsverband Bad Soden am Taunus sowie der Kreisbrandinspektor und der diensthabende Kreisbrandmeister alarmiert. Die Feuerwehr Bad Soden am Taunus rückt daraufhin mit dem Löschzug, bestehend aus einem Einsatzleitwagen, einem Löschgruppenfahrzeug, der Drehleiter und einem zweiten Löschgruppenfahrzeug in dieser Reihenfolge aus. Somit wird eine Einsatzstärke von mindestens 21 Feuerwehrleuten erreicht. Je nach Einsatz können dann noch weitere Fahrzeuge nachrücken oder weitere Feuerwehren alarmiert werden (z.B. zweite Drehleiter).
Wenn diese Meldung für die Stadtteile Altenhain und Neuenhain gilt, rücken, zusätzlich zu der jeweiligen Stadtteilfeuerwehr von der Feuerwehr Bad Soden am Taunus folgende Fahrzeuge in dieser Reihenfolge aus: Drehleiter, Löschgruppenfahrzeug, Einsatzleitwagen und zweites Löschgruppenfahrzeug.
Wenn allerdings eine Brandmeldeanlage in größeren, besonders gefährdeten Objekten in Bad Soden am Taunus angeschlagen hat, werden sofort alle 3 Feuerwehren der Stadt Bad Soden am Taunus alarmiert, um von Anfang an über eine große Einsatzstärke zu verfügen.
        </p>
        <p id="vu">
            Meldung <strong>'Verkehrsunfall mit eingeklemmter Person'</strong> im Stadtgebiet Bad Soden am Taunus.
Nun rückt der Hilfeleistungszug der Feuerwehr aus. Dieser besteht aus dem Rüstwagen und dem Hilfeleistungslöschgruppenfahrzeug. Je nach Einsatzort kann es sein, dass noch das Großtanklöschfahrzeug ausrückt, um die Wasserversorgung notfalls im Pendelverkehr sicher zu stellen. Da bei Verkehrsunfällen wassergefährdende Stoffe auslaufen, kommt meistens noch der Gerätewagen-Gefahrgut zum Einsatz, der unter anderem Bindemittel verladen hat. 
Sollte dieser Alarm für die Stadtteilwehren ausgehen, rücken von Bad Soden am Taunus die selben Fahrzeuge aus, wie im Bad Sodener Einsatzgebiet.
        </p>
        <p id="tuer">
            Meldung <strong>'Türöffnung für Rettungsdienst, hilflose Person hinter verschlossener Wohnungstür'</strong>. 
Neben einem Rettungswagen und eventuell einem Notarzt, rückt die Feuerwehr mit dem Rüstwagen und der Drehleiter aus.
        </p>
        <p>
            Dies sind nur einige Beispiele. Bei <strong>überörtlichen Einsätzen</strong> rückt in den meisten Fällen nur das vor Ort nicht vorhandene <strong>Sonderfahrzeug</strong> aus: Drehleiter, Rüstwagen oder Tanklöschfahrzeug. 
Bei einem Alarm im Main-Taunus-Zentrum rückt die Feuerwehr Bad Soden am Taunus jedoch mit Drehleiter, Tanklöschfahrzeug und dem Löschgruppenfahrzeug aus, damit zusammen mit den Feuerwehren Sulzbach und Schwalbach zwei Löschzüge an der Einsatzstelle sind.
        </p>
