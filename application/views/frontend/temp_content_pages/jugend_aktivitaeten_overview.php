        	<h1 class="module">Kanutour der Jugendfeuerwehr Bad Soden</h1>
            <div class="article">
                <p><img src="<?=base_url('images/content/content_jugend/content_jugend_kanu.jpg')?>" /></p>
                <p>
                    Am vergangenen Samstag, den 19. Juni fuhren acht Jugendliche und sechs Betreuer der Jugendfeuerwehr Bad Soden nach Wetzlar, um von dort aus eine Kanutour zu starten. 
                    Anfangspunkt war gegen 10:00 Uhr der Fluss-Kilometer 9.6, eine Fischerhütte an der Lahn. Nach einer Einweisung in die Kanus und in die Sicherheitsbestimmungen auf dem Fluss, 
                    wurden die Kanus verteilt und eifrig losgepaddelt. Während die Reise auf der Lahn in Richtung Solms ging, bauten derweil zwei Betreuer die Schlafmöglichkeiten auf dem Zeltplatz auf, 
                    der direkt an der Schlussanlegestelle des Tages bei Kilometer 23 lag.
                </p>
                <p>
                    Das weitere Tagesprogramm wurde mit diversen Ballspielen und gemütlichem Beisammensein gefüllt. Den Tag ließ man bei einem Grillen um das Lagerfeuer ruhig ausklingen, 
                    da alle Teilnehmer müde vom Paddeln waren.
                </p>
                <p>
                    Am nächsten Morgen ging es nach einem ausgiebigem Frühstück auch direkt weiter. Nun mussten die Jugendlichen noch einmal zeigen, was in Ihnen steckt, da eine Strecke von rund 
                    16 Kilometern bewältigt werden musste. Nach gut fünf Stunden auf dem Wasser erreichte die Mannschaft den endgültigen Bestimmungsort in Weilburg. Hier wurden die erschöpften, 
                    aber gut gelaunten Jugendlichen empfangen, die beim Abschlussgrillen einiges zu Erzählen hatten.
