        	<h1 class="module">Zeltlager in Kell am See</h1>
            <div class="article">
                <p>
                    In diesem Jahr fuhren die drei Stadtteile der Jugendfeuerwehr Bad Soden am Taunus vom 22. – 31. Juli nach Kell am See im Süden von Rheinland-Pfalz. Insgesamt haben 21 
                    Jugendliche und 9 Betreuer am alljährlichen Zeltlager teilgenommen.
                </p>
                <p>
                    Die Jugendlichen wurden am Dienstag nach der zwei stündigen Autofahrt zum Zeltplatz durch Stadtjugendfeuerwehrwart und Lagerleiter Sebastian Basten begrüßt und konnten sich 
                    anschließend in Gruppen einteilen, die dazu dienten, die Ordnung im Lager aufrecht zu erhalten. Das bedeutet, jede Gruppe (zu je 4 Jugendlichen und einem Betreuer) hat einen 
                    Dienst durchzuführen, der täglich rotiert. Es gab insgesamt fünf Dienste: 1. Küchen und Getränke Dienst; 2. Ordnungsdienst; 3. Spüldienst; 4. Nachtwache und Feuerdienst und 
                    der fünfte Dienst war, nach der Nachtwache, ein verdienter freier Tag.
                </p>
                <p><img src="<?=base_url('images/content/content_jugend/content_jugend_zeltlager.jpg')?>" /></p>
                <p>
                    Da bereits am Montag der Zeltplatz von den Betreuern und zwei älteren Jugendlichen aufgebaut wurde, konnten nach der Begrüßung alle ihre Zelte beziehen.
                    Am Dienstagnachmittag stand dann eine Wanderung um den nahe gelegenen See an und abends wurde traditionell das Lagerfeuer entzündet, das das ganze Zeltlager über brennen soll. 
                    In den folgenden Tagen standen Schwimmbadbesuche, ein Ausflug in die Stadt Trier und der Höhepunkt der ersten Woche, der Besuch in einem "Kletterwald", was für die Jugendlichen 
                    eine große Herausforderung war, da sie sich selbst sichern mussten, auf dem Programm. In der Nacht zum Samstag gab es dann überraschend einen Fackellauf.
                </p>
                <p>
                    Wie jedes Jahr gab es am Sonntag, nicht wie üblich um 08:30, sondern erst ab 10:00 Uhr Frühstück. Der weitere Tag wurde im Lager mit sportlichen Freizeitaktivitäten und Ausruhen verbracht.
                </p>
                <p>
                    Am Montag besuchten wir eine Flugausstellung in Hermeskeil, wo ca. 100 originale Flugzeuge und Hubschrauber ausgestellt sind. Am Dienstag ging es noch mal zur Abkühlung ins Schwimmbad 
                    und am Mittwoch fand die Lagerolympiade mit verschiedenen, teils selbst erfundenen Disziplinen, statt. Zum Abschluss gab es zum Abendessen selbst gemachte Hamburger und eine Schlussrunde, 
                    in der jeder Gelegenheit hatte, etwas über das Zeltlager loszuwerden. Am Ende waren sich alle einig, alles in allem, war es, für Betreuer sowie für die Jugendlichen, wieder ein sehr 
                    schönes Zeltlager. Das Wetter war meistens auf unserer Seite und die Stimmung war immer gut. Am Donnerstag bauten dann alle gemeinsam die Zelte wieder ab und gegen 14:00 Uhr wurden die 
                    Jugendlichen von einem Reisebus abgeholt und in ihre jeweilige Feuerwehr gebracht.                </p>
                </p>
