<h1>Gesetzliche Grundlagen</h1>
    <p>Nach dem Hessischen Gesetz über den Brandschutz, den Katastrophenschutz und die Allgemeine Hilfe (HBKG), müssen die Kommunen den <strong>Gefahrenschutz</strong> in ihrer Gebietskörperschaft <strong>sicherstellen</strong>. 
    Die Stadt Bad Soden am Taunus bedient sich hierzu ihrer <strong>drei Feuerwehren</strong> aus Bad Soden am Taunus, Neuenhain und Altenhain.</p>
    <p>Wir sind als freiwillige Feuerwehr gehalten, die <strong>Sicherheit der Bevölkerung</strong> zu garantieren und zwar in den Bereichen:
        <ul>
            <li>Vorbeugender Brandschutz</li> 
            <li>Abwehrender Brandschutz </li>
            <li>Technische Unfallhilfe </li>
            <li>Katastrophenschutz </li>
            <li>Allgemeine Hilfe</li>
        </ul>
    </p>
    <p>Die Feuerwehren sind somit eine <strong>kommunale Institution</strong>, die nach <strong>geltendem Recht</strong> arbeitet. Da bei Einsätzen Rechtsmittel des Grundgesetzes, ähnlich wie bei der Polizei, verletzt werden, wie z.B.:
        <ul>
            <li>Artikel 8, "Versammlungsfreiheit"</li> 
            <li>Artikel 13, "Unverletzbarkeit der Wohnung"</li> 
        </ul>
    </p>
    <p>müssen die oberen Führungskräfte der Feuerwehr (Stadtbrandinspektoren, Wehrführer und deren Stellvertreter) <strong>vom Magistrat ernannte Ehrenbeamte</strong> sein.</p>
    <p>Die Feuerwehr Bad Soden am Taunus wurde außerdem 1974 zur <strong>Stützpunktfeuerwehr</strong> des Main-Taunus-Kreises ernannt. Dies bedeutet, dass weitere Aufgaben von uns bewältigt werden müssen. Vom <strong>Ausrücken</strong> der Drehleiter oder des Rüstwagens in unsere <strong>Nachbarkommunen</strong>, bis zur <strong>Bereitschaft des Gefahrstoffzuges</strong> für den gesamten Kreis, reicht die Palette. Eine weitere Stützpunktfeuerwehr im Main-Taunus-Kreis ist die Feuerwehr Hofheim am Taunus.</p>
