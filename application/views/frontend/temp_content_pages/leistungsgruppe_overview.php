        <p>
            Auch bei der Feuerwehr werden, ähnlich wie in Sportvereinen, alljährlich Leistungswettkämpfe veranstaltet, die von Kreis-, über Bezirk- bis zum Landesentscheid führen.
            Im Jahr 2008 wurde die vom Land Hessen neu ausgearbeitete Leistungsübung vorgestellt und 2009 erstmals landesweit durchgeführt.
            Mit einer Staffel (6 Personen) oder einer Gruppe (9 Personen), die die Grundlagen der Feuerwehrtaktik bilden, müssen verschiedene Leistungsteile absolviert werden.
        </p>  
        <p>
            Zunächst müssen 15 feuerwehrtechnische Fachfragen aus den verschiedensten Wissensgebieten, im Multiple-Choice-Verfahren beantwortet werden. Je nach zu erreichendem Abzeichen kann sich die Zahl der zu beantworteten Fragen, über die Jahre bis auf ca. 50 erhöhen.<br />
            So wird im ersten Jahr das Leistungsabzeichen der Stufe Eisern verliehen.<br />
            Im zweiten Jahr mit 16 zusätzlichen Fragen die Stufe Bronze.<br />
            Im dritten Jahr mit 32 zusätzlichen Fragen die Stufe Silber.<br />
            Im vierten Jahr mit 32 zusätzlichen Fragen und verkürzter Beantwortungszeit die Stufe Gold.<br />
            Im fünften Jahr kann das Goldene Leistungsabzeichen mit Zusatzkennzeichnung für 5-malige Teilnahme erworben werden.<br />
            Im zehnten Jahr kann das Goldene Leistungsabzeichen mit Zusatzkennzeichnung für 10-malige Teilnahme erworben werden.<br />
            Als Voraussetzung gilt das Bestehen des "Extrabogens" (Bronze, Silber oder Gold) im jeweiligen Jahr.
        </p>  
        <p>
            Im praktischen Teil wird nun ein Brandeinsatz mit Innenangriff simuliert.
            Hier gilt es, innerhalb der vorgegebenen Zeit eine vermisste Person in Form einer Puppe im Gebäudeinnern zu finden und zu befreien.<br />
            Das Gebäude wird hier durch einen Kriechgang mit angebauter Tür dargestellt, die vorschriftsmäßig geöffnet und begangen werden muss.
            Im „Wohnzimmer“ angekommen muss zunächst noch eine Brandbekämpfung durchgeführt werden, es sind hier drei Fallklappen umzuspritzten, bevor die Puppe ins Freie gebracht werden kann.
        </p>
        <p>
            Im zweiten Angriff wird dann ein C-Strahlrohr von der Steckleiter aus zum Einsatz gebracht, wobei auch drei Fallklappen umgespritzt werden.<br />            		
            Besonderes Augenmerk wird hier auf die Einhaltung der geltenden Dienstvorschriften und die saubere Abwicklung der Funkgespräche und Rückmeldungen gelegt.<br />            
            In diesem Jahr stellte die Mannschaft aus Bad Soden zwei Staffeln für den Wettkampf, wobei es der ersten Mannschaft wieder gelang, den Kreismeistertitel zu verteidigen.
            Die zweite Staffel folgte dann zugleich auf den zweiten Platz. Eine Gruppe aus Altenhain, welche mit Personal aus dem Stadtteil Neuenhain zusätzlich verstärkt wurde, konnte sich 2012 den vierten Platz sichern - herzlichen Glückwunsch!
        </p>
