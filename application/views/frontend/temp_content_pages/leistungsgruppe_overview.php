            <div class="article">
                <p> 
                    Auch bei der Feuerwehr werden, ähnlich wie in Sportvereinen, alljährlich Leistungswettkämpfe veranstaltet, die von Kreis-, über Bezirks- bis zum Landesentscheid führen. 
                    Im Jahr 2008 wurde die vom Land Hessen neu ausgearbeitete Leistungsübung vorgestellt und 2009 erstmals landesweit durchgeführt.
                </p>
                <p> 
                    Mit einer Staffel (6 Personen) oder einer Gruppe (9 Personen), welche die Grundeinheiten der Feuerwehrtaktik bilden, müssen verschiedene Leistungsteile absolviert werden. 
                    Dabei wird sowohl das theoretische Wissen der Mitglieder als auch das praktische Können abgefragt. Nur wer beide Teile beherrscht darf auf das Siegerpodest.
                </p>
            </div>
            <h1 class="module">Der theoretische Teil</h1>
            <div class="article" id="anker_theorie">  
                <p>
                    Zunächst müssen feuerwehrtechnische Fachfragen aus den verschiedensten Wissensgebieten im Multiple-Choice-Verfahren beantwortet werden. 
                    Je nach zu erreichendem Abzeichen kann sich die Zahl der zu beantworteten Fragen über die Jahre bis auf ca. 50 erhöhen.
                </p>
                <ul>
                    <li>Im ersten Jahr startet man mit 15 Fragen für das Leistungsabzeichen der Stufe <strong>Eisern</strong>.</li>
                    <li>Im zweiten Jahr mit 16 zusätzlichen Fragen für die Stufe <strong>Bronze</strong>.</li>
                    <li>Im dritten Jahr mit 32 zusätzlichen Fragen für die Stufe <strong>Silber</strong>.</li>
                    <li>Im vierten Jahr mit 32 zusätzlichen Fragen und verkürzter Beantwortungszeit für die Stufe <strong>Gold</strong>.</li>
                    <li>Im fünften Jahr kann das Goldene Leistungsabzeichen mit Zusatzkennzeichnung für 5-malige Teilnahme erworben werden.</li>
                    <li>Im zehnten Jahr kann das Goldene Leistungsabzeichen mit Zusatzkennzeichnung für 10-malige Teilnahme erworben werden.</li>
                    <li>Als Voraussetzung gilt das Bestehen des "Zusatzbogens" (Bronze, Silber oder Gold) im jeweiligen Jahr.</li>
                </ul>
           </div>
           <h1 class="module" id="anker_praxis">Der praktische Teil</h1>
           <div class="article">
                <p>
                    Im praktischen Teil wird nun ein Brandeinsatz mit Innenangriff simuliert. Hier gilt es, innerhalb der vorgegebenen Zeit eine vermisste Person 
                    in Form einer Puppe im Gebäudeinnern zu finden und zu befreien. Das Gebäude wird hier durch einen Kriechgang mit angebauter Tür dargestellt, die vorschriftsmäßig geöffnet 
                    und begangen werden muss. Im "Wohnzimmer" angekommen muss zunächst eine Brandbekämpfung durchgeführt werden. Es sind hier drei Fallklappen umzuspritzen, 
                    bevor die Puppe ins Freie verbracht werden kann.
                </p>
                <p>
                    Im zweiten Angriff wird dann ein C-Strahlrohr von der Steckleiter aus zum Einsatz gebracht, wobei auch drei Fallklappen umgespritzt werden müssen.
                    Besonderes Augenmerk wird auf die Einhaltung der geltenden Dienstvorschriften und die saubere Abwicklung der Funkgespräche und Rückmeldungen gelegt.
                </p>
                <p>
                    Auch 2015 tritt die Mannschaft der Leistungsgruppe bei den Wettbewerben an. Den Kreisentscheid am 17.05.2015 in Wiesbaden Nordenstadt sowie den Bezirksentscheid am 27.06.2015 in Liederbach am Taunus konnte bereits gewonnen werden. Somit tritt die Mannschaft am 13.09.2015 zum „Endspiel", dem Landesentscheid, in Lorsch (Landkreis Bergstraße) an.
                </p>
            </div>
            
