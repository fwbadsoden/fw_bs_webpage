                <h1 class="first">Ausbildung in der Jugendfeuerwehr</h1>
                <p>
                    Die Ausbildung reicht vom normalen Übungsdienst Montag abends bis hin zur Herbstabschlussübung zusammen mit den zwei Stadtteiljugendfeuerwehren 
                    Altenhain und Neuenhain. In den Montagsdiensten werden theoretische Grundlagen beigebracht, die in der Praxis dann anschaulich verdeutlicht und ausprobiert werden können. 
                    In den Wintermonaten findet meistens keine Praxis statt. 
                </p>
