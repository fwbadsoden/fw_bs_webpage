        	<h1 class="module">Berufsfeuerwehrtag 2013</h1>
            <div class="article">
                <p>
                    Am Samstag, den 31.08.2013 fand der Berufsfeuerwehrtag der drei Jugendfeuerwehren aus Bad Soden, Altenhain und Neuenhain
                    in der Feuerwache Bad Soden statt. Die Jugendlichen sollten einen Tag lang den Alltag eines Berufsfeuerwehrmannes bzw. einer Berufsfeuerwehrfrau miterleben.
                    So versammelten sich morgens um 09:00 Uhr 34 Jugendliche im Hof, um ihren Dienst anzutreten und ihre Fahrzeuge zu übernehmen.
                </p>
                <p>
                    Nach dem Frühstück stand eine praktische Übung mit Stationsausbildung auf dem Programm. Hierbei wurde Strahlrohrtraining, das Bedienen von Gerätschaften, Führungskräfteausbildung 
                    sowie das Absperren von Unfallstellen geübt.
                </p>
                <p><img src="<?=base_url('images/content/content_jugend/content_jugend_C.jpg')?>" /></p>
                <p>
                    Nach dem Mittagessen sollte es eigentlich zum Dienstsport gehen, doch drei Fahrzeuge wurden durch eine Ölspur in Neuenhain vom Sport befreit. Nachmittags fand eine Unterrichtseinheit zum Thema 
                    Sprechfunk statt. Da die Jugendlichen mit eigenen Funkgeräten übten, musste der Umgang damit gelernt sein. Die Theoriestunde wurde mit dem Gong beendet - Einsatz! Durch den Lautsprecher ertöhnt 
                    "Alarm für Alle. Flugzeugabsturz". Als die Jugendlichen zu ihren Klamotten rannten, staunten sie nicht schlecht, als das Jugendrotkreuz Bad Soden und das Jugend-THW aus Hofheim ebenfalls 
                    einsatzbereit auf dem Hof standen. Zusammen mit den beiden Hilfsorganisationen ging es dann mit Blaulicht und Martinshorn zur neuen Grundschule in Bad Soden. Dort wurde ein Flugzeugabsturz simuliert.
                </p>
                <p>
                    Es galt mehrere Verletzte zu versorgen, einen brennenden PKW und das aus Holz gebaute Cockpit zu löschen sowie das aus Containern gebaute Flugzeug nach Personen abzusuchen. 
                    Das Jugend-THW befreite mit schweren Hebekissen eine Person, welche unter einem PKW eingeklemmt war. Die Jugendlichen arbeiteten die Einsatzstellen nach und nach professionell ab und 
                    konnten nach gut 35 Minuten das Einsatzende melden.
                </p>
                <p>
                    Anschließend wurde gemeinsam gegrillt und der Einsatz konnte von den Jugendlichen noch einmal Revue passiert werden. Lange Zeit zum Verschnaufen hatte der Sodener Feuerwehr-Nachwuchs nicht. 
                    Um 20:25 Uhr, als die Mannschaft sich einen ruhigen DVD Abend machen wollte, wurden sie zu einem Wohnungsbrand in die Straße Zum Quellenpark alarmiert. Dort wurden gleich mehrere Trupps
                    unter Atemschutz (eigens für die Jugendfeuerwehr gebaute Atemschutzgeräte) zur Personensuche im Geäbude eingesetzt. Im Disconebel mussten die Jugendlichen beweisen, dass sie zusammenbleiben 
                    und sich auch unter stress verständigen und den Auftrag ausführen können. Zwei Personen konnten über das Treppenhaus schließlich gerettet werden.
                </p>
                <p>
                    In der Wache angekommen, wurden die Fahrzeuge wie nach jedem Einsatz wieder "fit" gemacht. Anschließend konnte die DVD fertig geschaut werden. Um 23:00 Uhr wurde ein Löschfahrzeug zu einer 
                    Türöffnung zum alten Reitstall in die Kronbergerstr. gerufen. Dort war ein Gasgeruch gemeldet worden. Nachdem die Tür geöffnet worden war stelle sich heraus, dass zusätzlich ein defektes Rohr 
                    zu einem Wasserrohrbruch geführt hatte. Sogleich alarmierte das erste Löschfahrzeug ein weiteres nach, welches an die Einsatzstelle nachrückte und den Keller leer pumpte. Die erste Gruppe 
                    drehte den Gashahn ab und rettete eine Person aus dem Gebäude.
                </p>
                <p>
                    Wenn die Jugendlichen jetzt glaubten, dass die Nacht eine ruhige wird, so täuschten sie sich. Um 01:00 Uhr in der Früh wurde eine Person in der Nähe des Eberhad-Preis-Weg vermisst. Die beiden 
                    Zugführer Moritz Thörner und Niklas Witzel formierten ihre Kräfte und ließen den Bereich strukturiert absuchen - mit Erfolg. Eine nicht mehr gehfähige Person wurde aufgefunden und konnte aus dem 
                    Gebiet heraus an den Rettungsdienst übergeben werden.
                </p>
                <p>
                    Nachdem Jugendliche und Betreuer nun doch ein paar Stunden schlafen konnten, löste zum Schluss noch einmal eine Brandmeldeanlage in der Kahlbachhalle in Altenhain aus.
                    Anschließend wurden die Fahrzeuge gewartet, gefrühstückt und die Schlafräume wieder hergerichtet. Um 10:00 Uhr konnten die Jugendlichen in ihren wohlverdienten "Dienstschluss" treten.
                    Trotz des Einsatzes am Samstagmorgen in der KiTa "Unterm Regenbogen", den die Einsatzabteilungen aus Bad Soden bewältigen mussten, konnte der BF Tag der Jugendfeuerwehr wie geplant stattfinden. 
                    Die Betreuer bedanken sich bei allen Helfern, ohne die dieser Umfang und diese Vielzahl von Aufgaben nie zu bewältigen gewesen wäre.                
                </p>
