                <h1 class="first">Die Jugendfeuerwehr Bad Soden a.Ts.</h1>
                <p>
                Die Jugendfeuerwehr Bad Soden am Taunus ist neben der Einsatz- und der Alters- und Ehrenabteilung die dritte Abteilung in der Freiwilligen Feuerwehr Stadt Bad Soden am Taunus.
                Mit Hilfe von praktischen und theoretischen Übungen werden die Kinder und Jugendlichen an die Feuerwehrtechnische Ausbildung herangeführt. 
                Darüber hinaus bleibt auch viel Zeit für gemeinsame Aktivitäten und ganz viel Spaß.</p>
                <p>
                    Jugendliche ab 10 Jahre, egal ob Mädchen oder Jungs, können in die Jugendfeuerwehr eintreten. Der Dienst findet zu Schulzeiten Montags von 18:00 Uhr bis 20:00 Uhr statt. 
                    Aktuell sind 15 Jugendliche in der Jugendfeuerwehr. Bis zum 17. Lebensjahr ist man dann Mitglied in der Jugendfeuerwehr. Während dieser Zeit erlernt man feuerwehrtechnisches Grundwissen, 
                    um auf den späteren Dienst in der Einsatzabteilung gut vorbereitet zu sein. Allerdings wird auch auf die allgemeine Jugendarbeit Wert gelegt. Dies zeigt sich an den vielen Ausflügen die 
                    die Jugendfeuerwehr veranstaltet. Hierzu zählen zum Beispiel Besuche im Schwimmbad, Ausflüge zur Bowlingbahn oder in die Kletterhalle. Die Krönung ist jedoch das jährlich stattfindende 10-tägige Zeltlager 
                    mit den Jugendfeuerwehren Altenhain und Neuenhain.
                </p>
