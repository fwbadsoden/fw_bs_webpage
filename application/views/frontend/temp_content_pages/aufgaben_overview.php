                <h1>Warum eine Feuerwehr?</h1>
                <p>
                    Nach dem Hessischen Gesetz über den Brandschutz, den Katastrophenschutz und die Allgemeine Hilfe (HBKG), müssen die Kommunen den Gefahrenschutz in ihrer Gebietskörperschaft sicherstellen. 
                    Zu diesem Zweck unterhält die Stadt Bad Soden am Taunus drei öffentliche Feuerwehren: Bad Soden am Taunus, Neuenhain und Altenhain. Die drei Feuerwehren, die hauptsächlich aus ehrenamtlichen 
                    Mitgliedern bestehen, haben den Auftrag die Sicherheit der Bevölkerung zu garantieren. Dabei werden sie von einigen wenigen hauptamtlichen Kräften unterstützt.
                </p>
            </div>
            <h1 class="module" id="anker_aufgaben">Die Aufgaben der Feuerwehr</h1>
            <div class="article"> 
            	<h1 class="einsatzIcon_prefire">Vorbeugender Brandschutz</h1>  
                <p>
                    Die beste Methode der Brandbekämpfung ist, den Brand gar nicht erst entstehen zu lassen. Unter den vorbeugenden Brandschutz fallen die Abnahme von öffentlichen Neubauten im Sinne des Brandschutzes, 
                    die Brandschutzerziehung in Kindergärten und Schulen sowie Brandsicherheitsdienste auf größeren Veranstaltungen oder Festen (zum Beispiel das Sommernachtsfest in Bad Soden am Taunus).
                </p>
            	<h1 class="einsatzIcon_fire">Abwehrender Brandschutz</h1>  
                <p>
                    Damit ist der klassische Löscheinsatz der Feuerwehr gemeint. Allerdings unterscheiden wir hierbei zwischen einem Nutz- und einem Schadensfeuer. Wenn ein Feuer kontrolliert und bestimmt verläuft, 
                    zum Beispiel das angemeldete Verbrennen von Gartenabfällen, so ist dies ein Nutzfeuer. Gerät dieses Feuer jedoch außer Kontrolle, wird es schnell gefährlich und man spricht von einem Schadensfeuer.
                </p>
            	<h1 class="einsatzIcon_tools">Technische Unfallhilfe</h1>  
                <p>
                    Neben der Brandbekämpfung wird die Feuerwehr auch zu anderen Situationen hinzugezogen, bei denen Personen oder Tiere in Gefahr sind. Dazu zählen unter anderem Verkehrs-, Haus- oder Arbeitsunfälle 
                    bei denen Opfer eingeklemmt oder verschüttet wurden. In diesen Fällen rückt die Feuerwehr aus, um andere Rettungseinheiten zu unterstützen und diese Personen oder Tiere mit technischen Geräten zu befreien.
                </p>
            	<h1 class="einsatzIcon_gas">Katastrophenschutz</h1>  
                <p>
                    Da das Gesetz vorsieht, dass die Kommunen die Bevölkerung auch vor einer allgemeinen Gefahr zu schützen haben, rückt die Feuerwehr auch im Katastrophenfall aus. 
                    Dazu zählen vor allem Hochwasser, aber auch die Unterstützung bei Tierseuchen (BSE, MKS, usw.).
                </p>
            	<h1 class="einsatzIcon_saw">Allgemeine Hilfe</h1>  
                <p>
                   Unter diesen Punkt fallen alle Einsätze, die keiner der oben genannten Gruppen zugeordnet werden können. Beispiele sind Türöffnungen bei akuter Gefahr, akute Hilfe bei Unwetterschäden oder 
                   die große Palette der Gefahrstoffunfälle im ABC-Bereich.
                </p>
           </div>
           <h1 class="module" id="anker_gesetze">Gesetzliche Lage</h1>
           <div class="article">
                <p>
                    Die Feuerwehren sind somit eine kommunale Institution, die nach geltendem Recht arbeitet. Da bei Einsätzen Rechtsmittel des Grundgesetzes, ähnlich wie bei der Polizei, verletzt werden, wie z.B.:
                </p>
                <ul>
                	<li>Artikel 8, "Versammlungsfreiheit"</li>
                    <li>Artikel 13, "Unverletzbarkeit der Wohnung"</li>
                </ul>
                <p>
                    müssen die oberen Führungskräfte der Feuerwehr (Stadtbrandinspektoren, Wehrführer und deren Stellvertreter) vom Magistrat ernannte Ehrenbeamte sein.
				</p>
                <p>
                    Die Feuerwehr Bad Soden am Taunus wurde außerdem 1974 zur Stützpunktfeuerwehr des Main-Taunus-Kreises ernannt. Dies bedeutet, dass weitere Aufgaben von uns bewältigt werden müssen. 
                    Vom Ausrücken der Drehleiter oder des Rüstwagens in unsere Nachbarkommunen, bis zur Bereitschaft des Gefahrstoffzuges für den gesamten Kreis, reicht die Palette. 
                    Eine weitere Stützpunktfeuerwehr im Main-Taunus-Kreis ist die Feuerwehr Hofheim am Taunus.
                </p>
            </div>
            
