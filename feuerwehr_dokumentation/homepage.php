<h2>Willkommen bei feuerwehr-bs.de</h2>

<p>Die Webapplikation wurde auf Basis des Frameworks CodeIgniter erstellt.<br/> 
Hierbei handelt es sich um ein Entwicklungsframework f&uuml;r Webanwendungen unter der Nutzung von PHP und MySQL.<br/> 
Das Framework verfolgt den MVC Ansatz und ist dementsprechen in Models, Controller und Views strukturiert.<br /> 
N&auml;here Infos hierzu gibt es im CodeIgniter Userguide unter <a href="http://ellislab.com/codeigniter/user-guide/index.html" target="_blank">http://ellislab.com/codeigniter/user-guide/index.html</a>.</a></p>

